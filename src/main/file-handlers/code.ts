export const codeHandler: FileHandler = {
  extensions: codeExtensions,

  async read(filePath: string): Promise<FileResult> {
    const content = fs.readFileSync(filePath, "utf-8");
    return {
      path: filePath,
      name: basename(filePath),
      content,
    };
  },
};
